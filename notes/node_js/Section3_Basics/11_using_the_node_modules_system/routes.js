const fs = require("fs");

const requestHandler = (req, res) => {
    const url = req.url;
    const method = req.method;
    if (url === "/") {
        res.write("<html>");
        res.write("<head><title>Enter message</title></head>");
        res.write(
            '<body><form action="/message" method="POST"><input type="text" name="message"><button type="submit">Send</button></form></body>'
        ); 
        res.write("</html>");
        return res.end(); 
    }
    if (url === "/message" && method === "POST") {
        const body = [];
        req.on('data', (chunk) => { 
            console.log(chunk);
            body.push(chunk);  
        });
        return req.on('end', () => { 
            const parsedBody = Buffer.concat(body).toString(); 
            console.log('parsedBody : ' + parsedBody);
            const message = parsedBody.split('=')[1]; 
            fs.writeFileSync('message.txt', message);   // blocking code // sync stands for synchronous, this will block the event loop and wait for the file to be written. This is Blocking Code. There is a another version of this code fs.writeFile(message.txt, message, () => {}); which is a non-blocking code.
            fs.writeFile('message.txt', message, err => {
                res.statusCode = 302;
                res.setHeader('Location', '/');
                return res.end();
            }); // non-blocking code
        });
    }
    res.setHeader("Content-Type", "text/html");
    res.write("<html>");
    res.write("<head><title>My First Page</title></head>");
    res.write("<body><h1>Hello from my Node.js Server!</h1></body>");
    res.write("</html>");
    res.end();
};

